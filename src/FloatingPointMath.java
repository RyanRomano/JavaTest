
public class FloatingPointMath {
	public static void main (String[] args) {
		float numberOne = 10.5f;
		float numberTwo = 1.5f;
		
		System.out.println("Sum equals " + (numberOne + numberTwo));
		System.out.println("Difference equals " + (numberOne - numberTwo));
		System.out.println("Product equals " + (numberOne * numberTwo));
	}										
}